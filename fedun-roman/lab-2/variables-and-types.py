# Це змінна з типом int (ціле число)
age = 18

# Це змінна з типом float (десяткове число)
height = 1.89

# Це змінна з типом str (рядок)
name = "Fdn"

# Це змінна з типом bool (логічне значення)
is_student = True

# Виводимо значення та тип кожної змінної
print("Ім'я:", name)
print("Вік:", age)
print("Зріст:", height, "м")
print("Студент:", is_student)

print("Тип змінної 'age':", type(age))
print("Тип змінної 'height':", type(height))
print("Тип змінної 'name':", type(name))
print("Тип змінної 'is_student':", type(is_student))
