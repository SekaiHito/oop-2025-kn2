# ❌ Поганий приклад порушення SRP:
# Один клас робить одразу ТРИ речі:
# 1) зберігає дані в БД
# 2) валідовує користувача
# 3) зберігає інформацію про користувача
class User:
    def __init__(self, name):
        self.name = name

    def save_to_db(self):
        # Цей метод не повинен бути всередині User,
        # бо робота з БД — це інша відповідальність.
        print("Зберегли користувача в БД")

    def validate(self):
        # І це також окрема відповідальність —
        # валідація має бути в окремому класі.
        print("Перевірка користувача")


