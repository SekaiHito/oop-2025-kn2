print("Демонстрація базового синтаксису функцій в Python")

# Функція без параметрів
def greet():
    print("Привіт, світ!")

# Функція з одним параметром
def greet_person(name):
    print(f"Привіт, {name}!")

# Функція з кількома параметрами та значенням за замовчуванням
def greet_person_with_title(name, title="пан"):
    print(f"Привіт, {title} {name}!")

# Демонстрація виклику функцій
print("\n1. Виклик функції без параметрів:")
greet()

print("\n2. Виклик функції з одним параметром:")
greet_person("Володимир")

print("\n3. Виклик функції з параметрами за замовчуванням:")
greet_person_with_title("Володимир")
greet_person_with_title("Марія", "пані")

