# Демонстрація булевої логіки
age = int(input("\nВведіть ваш вік: "))

if age >= 18:
    print("Ви повнолітній")
else:
    print("Ви неповнолітній") 

# Демонстрація логічних операторів
print("\nДемонстрація логічних операторів:")

# Логічне "І"
result = (age >= 18) and (age <= 65)
print(f"Чи є вік в діапазоні від 18 до 65: {result}")

# Логічне "АБО"
result = (age < 18) or (age > 65)
print(f"Чи дитина або пенсіонер: {result}")

# Логічне "НЕ"
result = not (age >= 18)
print(f"Чи неповнолітній: {result}")



