print("Програма для обчислення факторіалу числа")
print("-" * 40)

# Отримання числа від користувача
n = int(input("Введіть число для обчислення факторіалу: "))

# Перевірка на від'ємне число
if n < 0:
    print("Факторіал від'ємного числа не існує")
else:
    # Обчислення факторіалу
    factorial = 1
    for i in range(1, n + 1):
        factorial *= i
    
    # Виведення результату
    print(f"Факторіал числа {n} = {factorial}") 