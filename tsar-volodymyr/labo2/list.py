# Ініціалізація списків
numbers = [1, 2, 15]
strings = ["Рядок 1", "Рядок 2"]
names = ["Максим", "Олександр", "Дарина"]  # Замінив усі імена

# Отримуємо друге ім’я за допомогою розпакування списку
_, second_name, _ = names

# Виведення результатів за допомогою f-рядків
print(f"Список чисел: {numbers}")
print(f"Список рядків: {strings}")
print(f"Друге ім’я у списку імен — {second_name}")



